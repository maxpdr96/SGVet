    public void logar() {
        String sql = "select * from funcionario where id = ? and idsenha = ?";

        try {
            pst = con.prepareStatement(sql);
            pst.setInt(1, Integer.parseInt(tfNome.getText()));
            pst.setString(2, tfSenha.getText());

            rs = pst.executeQuery();

            if (rs.next()) {
                Menu frm = new Menu();
                frm.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Usuario ou senha invalido!");
            }

        } catch (SQLException error) {

            JOptionPane.showMessageDialog(null, error);

        }

    }

